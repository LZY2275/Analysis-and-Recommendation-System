    public void setEnrollmentRatio(float enrollmentRatio) {
        this.enrollmentRatio = enrollmentRatio;
    }

    public float getSexRatio() {
        return sexRatio;
    }

    public void setSexRatio(float sexRatio) {
        this.sexRatio = sexRatio;
    }
